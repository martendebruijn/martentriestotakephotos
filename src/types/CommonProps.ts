/**
 * Possible icon types
 *
 * * `fas` - Font Awesome Solid
 * * `far` - Font Awesome Regular
 * * `fab` - Font Awesome Brands
 */
export type IconType = 'fas' | 'far' | 'fab'

/**
 * Default styling
 *
 * * `primary` - The primary color
 * * `secondary` - The secondary color
 * * `danger` - Use to signify a dangerous action or message
 * * `success` - Use to signify a successful action or message
 * * `warning` - Use to signify an action or message that can be problematic but
 *   isn't disastrous
 * * `info` - Use to signify a purely informational message
 */
export type Appereance =
  | 'primary'
  | 'secondary'
  | 'danger'
  | 'success'
  | 'warning'
  | 'info'
